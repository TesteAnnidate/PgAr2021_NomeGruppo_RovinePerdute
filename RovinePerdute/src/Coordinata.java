public class Coordinata {
    private int x;
    private int y;
    private int h;

    public Coordinata(int h, int y, int x){
        this.x = x;
        this.y = y;
        this.h = h;
    }

    //GETTERS
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getH() {
        return h;
    }
}
